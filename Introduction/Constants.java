public class Constants {
    public static void main(String[] args) {
        //Variables
        int number = 10;
        number = 20;
        System.out.println(number);

        final int number2 = 2; //final is for constants.
        //number2 =10; It cannot be reassigned because it is a constant.
        System.out.println(number2);
    }
}
